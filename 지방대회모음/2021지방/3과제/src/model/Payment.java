package model;

public class Payment {

	public int wh_no;
	public int peo;
	public int m_no;
	public int i_no;
	public String date;
	
	public Payment(int wh_no, int peo, int m_no, int i_no, String date) {
		this.wh_no = wh_no;
		this.peo = peo;
		this.m_no = m_no;
		this.i_no = i_no;
		this.date = date;
	}
	
	
}
